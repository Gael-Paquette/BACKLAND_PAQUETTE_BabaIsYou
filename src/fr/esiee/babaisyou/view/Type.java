package fr.esiee.babaisyou.view;

/**
 * The {@code Type} enum represents the different categories of images used in the game.
 * <p>
 * The possible values are:
 * <ul>
 *   <li>{@link #NOUNS} : Represents noun images</li>
 *   <li>{@link #OPERATORS} : Represents operator images</li>
 *   <li>{@link #PROPERTIES} : Represents property images</li>
 * </ul>
 * </p>
 */
public enum Type {
  /**
   * Represents noun images.
   */
  NOUNS,

  /**
   * Represents operator images.
   */
  OPERATORS,

  /**
   * Represents property images.
   */
  PROPERTIES
}
