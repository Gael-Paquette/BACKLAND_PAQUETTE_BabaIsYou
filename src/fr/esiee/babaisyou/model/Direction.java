package fr.esiee.babaisyou.model;

/**
 * Enumerates the possible directions in the game.
 * This enum defines four directions that can be used for movement or orientation within the game board.
 */
public enum Direction {
  /**
   * Represents the upward direction.
   */
  UP,

  /**
   * Represents the downward direction.
   */
  DOWN,

  /**
   * Represents the leftward direction.
   */
  LEFT,

  /**
   * Represents the rightward direction.
   */
  RIGHT;
}
